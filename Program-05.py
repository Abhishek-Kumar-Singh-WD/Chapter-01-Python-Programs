# This is a program to compare two numbers entered by the user.
# It prints True if n1 is greater than or equal to n2. If not, it prints False.
# This is a formula. [N1 >= N2]

number1 = int(input("Enter the first number : "))

number2 = int(input("Enter the second number : "))

result = number1 >= number2

print("The result is : ",result)
