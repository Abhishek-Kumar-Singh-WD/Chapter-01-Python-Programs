# This is a program to print the perimeter of a rectangle using the height and width entered by the user.
# This is a formula to calculate the perimeter of a rectangle. [P = (H + W) * 2]

height = float(input("Enter the height of a rectangle : "))

width = float(input("Enter the width of a rectangle : "))

perimeter = (height + width) * 2

print("The perimeter of a rectangle is : ",perimeter)
